class Solution{
    public:
    //Function to arrange all letters of a string in lexicographical 
    //order using Counting Sort.
    string countSort(string arr){
        vector<int> ar(26,0);
        for(auto i:arr){
            ar[i-'a']++;
        }
        string ans;
        for(int i=0;i<26;i++){
            while(ar[i]>0){
              ans.push_back(i+'a');
              ar[i]--;
            }
        }
        return ans;
    }
};


